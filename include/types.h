/*
 * ============================================================================
 * types.h — Mini-Linux 全局常量与公共类型
 * ============================================================================
 *
 * 本头文件被 VFS、Process、Shell 等所有模块共享。
 * 所有 magic number 集中定义在此，便于统一调整容量限制。
 *
 * 依赖：stddef.h（size_t）
 * ============================================================================
 */

#ifndef MINI_OS_TYPES_H
#define MINI_OS_TYPES_H

#include <stddef.h>

/* --------------------------------------------------------------------------
 * 容量限制常量
 * -------------------------------------------------------------------------- */

#define MAX_NAME     64    /* 单个文件/目录/进程名的最大字符数（含 '\0' 前） */
#define MAX_PATH     256   /* 路径字符串最大长度，如 "/home/user/file.txt" */
#define MAX_LINE     512   /* Shell 一次读入的命令行最大长度 */
#define MAX_CONTENT  4096  /* 单个虚拟文件可存储的最大字节数 */
#define MAX_PROCS    32    /* 进程表最多同时容纳的进程条目数 */
#define MAX_ARGS     16    /* 命令行 argv 数量 / 路径分段数量的上限 */
#define MAX_FDS      8     /* 预留：未来实现文件描述符表时使用 */

/* --------------------------------------------------------------------------
 * VFS 节点类型
 * --------------------------------------------------------------------------
 * 每个 vfs_node_t 必须是文件或目录之一，不支持符号链接等设备类型。
 */
typedef enum {
    NODE_FILE = 0,   /* 普通文件，内容存在 content[] 中 */
    NODE_DIR           /* 目录，子项通过 children 链表挂载 */
} node_type_t;

/* --------------------------------------------------------------------------
 * 进程状态（模仿 Linux ps 命令 STAT 列的单字符缩写）
 * --------------------------------------------------------------------------
 * R = Running（运行）
 * S = Sleeping（睡眠，本项目中暂未使用）
 * Z = Zombie（僵尸，被 kill 后的过渡状态）
 */
typedef enum {
    PROC_RUNNING = 0,
    PROC_SLEEPING,
    PROC_ZOMBIE
} proc_state_t;

#endif /* MINI_OS_TYPES_H */
